#ifndef PINS_H_
#define PINS_H_

/* Dot matrix (MAX7219 style) pins */
#define DOTMATRIX_DIN     11U
#define DOTMATRIX_CLK     13U
#define DOTMATRIX_CS      12U

/* Buzzer and fan pins */
#define BUZZER            6U
#define FAN               8U

/* Status LED pins */
#define LED_RED           2U
#define LED_BLUE           3U
#define LED_YELLOW         4U
#define LED_GREEN          5U

#endif /* PINS_H_ */